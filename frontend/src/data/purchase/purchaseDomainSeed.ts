function moneyIntra(taxable: number, freight = 0, other = 0, discount = 0, gstPct = 18) {
  const taxableAmount = Math.max(0, taxable - discount)
  const half = Number(((taxableAmount * gstPct) / 200).toFixed(2))
  const subtotal = taxable
  const totalAmount = Number((taxableAmount + half * 2 + freight + other).toFixed(2))
  return {
    currency: 'INR' as const,
    subtotal,
    discount,
    taxableAmount,
    cgst: half,
    sgst: half,
    igst: 0,
    freight,
    otherCharges: other,
    roundOff: 0,
    totalAmount,
  }
}

function moneyInter(taxable: number, freight = 0, other = 0, discount = 0, gstPct = 18) {
  const taxableAmount = Math.max(0, taxable - discount)
  const igst = Number(((taxableAmount * gstPct) / 100).toFixed(2))
  return {
    currency: 'INR' as const,
    subtotal: taxable,
    discount,
    taxableAmount,
    cgst: 0,
    sgst: 0,
    igst,
    freight,
    otherCharges: other,
    roundOff: 0,
    totalAmount: Number((taxableAmount + igst + freight + other).toFixed(2)),
  }
}

