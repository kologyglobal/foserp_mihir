const paramMap = new Map(seedQcParameters.map((p) => [p.id, p]))

export function getQcParameter(id: string): QcParameterMaster | undefined {
  return paramMap.get(id)
}

export function getQcParameterByCode(code: string): QcParameterMaster | undefined {
  return seedQcParameters.find((p) => p.parameterCode === code)
}

function planLine(
  id: string,
  planId: string,
  parameterId: string,
  sortOrder: number,
  overrides?: Partial<Pick<InspectionPlanLine, 'mandatoryOverride' | 'minValueOverride' | 'maxValueOverride' | 'targetValueOverride'>>,
): InspectionPlanLine {
  return {
    id,
    planId,
    parameterId,
    sortOrder,
    mandatoryOverride: overrides?.mandatoryOverride ?? null,
    minValueOverride: overrides?.minValueOverride ?? null,
    maxValueOverride: overrides?.maxValueOverride ?? null,
    targetValueOverride: overrides?.targetValueOverride ?? null,
  }
}

export { planLine }
