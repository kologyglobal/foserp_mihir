import type { BomTravelerDocument, BomTravelerRow } from '@/types/bomTravelerPreview'

function asm(
  id: string,
  level: number,
  bomNo: string,
  parent: string | null,
  itemNo: string,
  description: string,
  material: string,
  qty: number,
  unit: string,
  weight: number | null,
  totalWt: number | null,
  dims: string,
): BomTravelerRow {
  return {
    id,
    level,
    kind: 'assembly',
    bomNo,
    parentBomNo: parent,
    itemNo,
    description,
    materialGrade: material,
    qty,
    unit,
    weightKg: weight,
    totalWeightKg: totalWt,
    dimensionsSpec: dims,
    productionProcess: '',
    machineTool: '',
    qcInspection: '',
  }
}

function cmp(
  id: string,
  level: number,
  bomNo: string,
  parent: string | null,
  itemNo: string,
  description: string,
  material: string,
  qty: number,
  unit: string,
  weight: number | null,
  totalWt: number | null,
  dims: string,
): BomTravelerRow {
  return {
    id,
    level,
    kind: 'component',
    bomNo,
    parentBomNo: parent,
    itemNo,
    description,
    materialGrade: material,
    qty,
    unit,
    weightKg: weight,
    totalWeightKg: totalWt,
    dimensionsSpec: dims,
    productionProcess: '',
    machineTool: '',
    qcInspection: '',
  }
}

function proc(
  id: string,
  parentBomNo: string,
  itemNo: string,
  description: string,
  process: string,
  machine: string,
  qc: string,
): BomTravelerRow {
  return {
    id,
    level: null,
    kind: 'process',
    bomNo: 'PROC',
    parentBomNo,
    itemNo,
    description,
    materialGrade: '—',
    qty: null,
    unit: '—',
    weightKg: null,
    totalWeightKg: null,
    dimensionsSpec: '—',
    productionProcess: process,
    machineTool: machine,
    qcInspection: qc,
  }
}

