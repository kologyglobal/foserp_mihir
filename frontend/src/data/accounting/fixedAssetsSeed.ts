function assetBase(
  id: string,
  assetNumber: string,
  name: string,
  categoryId: string,
  categoryName: string,
  status: FixedAsset['status'],
  acquisitionCost: number,
  accumulatedDepreciation: number,
  extra: Partial<FixedAsset> = {},
): FixedAsset {
  const netBookValue = extra.netBookValue ?? Math.round((acquisitionCost - accumulatedDepreciation) * 100) / 100
  const residualValue = extra.residualValue ?? Math.round(acquisitionCost * 0.05)
  return {
    id,
    assetNumber,
    name,
    categoryId,
    categoryName,
    status,
    location: extra.location ?? 'Chakan Plant — Bay 1',
    plant: extra.plant ?? 'Chakan Plant',
    department: extra.department ?? 'Production',
    custodian: extra.custodian ?? 'Vikram Patil',
    acquisitionDate: extra.acquisitionDate ?? fyDate(4, 15),
    capitalizationDate: extra.capitalizationDate ?? (status === 'Under Construction' || status === 'Pending Capitalization' ? null : fyDate(4, 20)),
    acquisitionCost,
    residualValue,
    usefulLifeYears: extra.usefulLifeYears ?? 15,
    depreciationMethod: extra.depreciationMethod ?? 'Straight Line',
    accumulatedDepreciation,
    netBookValue,
    salvageValue: extra.salvageValue ?? residualValue,
    serialNumber: extra.serialNumber ?? null,
    manufacturer: extra.manufacturer ?? null,
    model: extra.model ?? null,
    vendorName: extra.vendorName ?? null,
    poNumber: extra.poNumber ?? null,
    invoiceNumber: extra.invoiceNumber ?? null,
    insurancePolicy: extra.insurancePolicy ?? null,
    insuranceExpiry: extra.insuranceExpiry ?? null,
    lastVerificationDate: extra.lastVerificationDate ?? null,
    nextVerificationDate: extra.nextVerificationDate ?? null,
    warrantyExpiry: extra.warrantyExpiry ?? null,
    isComponent: extra.isComponent ?? false,
    parentAssetId: extra.parentAssetId ?? null,
    currency: 'INR',
    company: COMPANY,
    createdBy: CREATED_BY,
    createdAt: extra.createdAt ?? fyDateTime(4, 15),
    modifiedAt: extra.modifiedAt ?? fyDateTime(7, 10),
    notes: extra.notes ?? null,
  }
}

