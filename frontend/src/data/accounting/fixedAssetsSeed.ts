function depLine(
  id: string,
  runId: string,
  assetId: string,
  assetNumber: string,
  assetName: string,
  categoryName: string,
  method: DepreciationLine['method'],
  period: string,
  openingWDV: number,
  depreciationAmount: number,
): DepreciationLine {
  const closingWDV = Math.round((openingWDV - depreciationAmount) * 100) / 100
  return {
    id,
    runId,
    assetId,
    assetNumber,
    assetName,
    categoryName,
    method,
    period,
    openingWDV,
    depreciationAmount,
    closingWDV,
    accumulatedDepreciation: 0,
    netBookValue: closingWDV,
  }
}

