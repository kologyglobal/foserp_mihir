function line(
  partial: Omit<BomLine, 'id' | 'lineNo' | 'issueMethod'> & {
    lineNo: number
    issueMethod?: BomLine['issueMethod']
  },
): BomLine {
  return {
    ...partial,
    id: `mfg-bom-line-${partial.lineNo}-${partial.componentItemCode}`,
    issueMethod: partial.issueMethod ?? (partial.supplyMethod === 'vendor_supplied' ? 'manual' : 'auto'),
  }
}

